package day04_variables;

public class NumbersExamples {
    public static void main(String[] args) {
//     byte
        byte  dateNumber = 31;
        System.out.println(dateNumber);

//      Short
        short    numberTwo = 200;
        System.out.println(numberTwo);
        System.out.println(012); // print

//     int
        int intNumber;  // declare an int variable called intNumber
        intNumber  =  10000; // assign 10000 to my intNumber variable
        System.out.println(intNumber);
//     long
        long fourthNumber = 2346964;
        System.out.println(fourthNumber);
        long bigNumber = 3_000_00000000000000l; // underscore makes the value readable
        System.out.println(bigNumber);




    }
}
