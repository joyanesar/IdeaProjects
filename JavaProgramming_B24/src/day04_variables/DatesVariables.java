package day04_variables;

public class DatesVariables {
    public static void main(String[] args) {




        int month = 07;
        int day   = 31;
        int year  = 2021;
        System.out.println("Today's date is /"+ month+"/" + day+"/" + year);


        month = 8;
        day   = 1;
        System.out.println("Tomorrows date is /"+ month+"/" + day+"/" + year);


    }
}
