package day03_Comments_escape_sequence;

public class EscapeSquence {
    public static void main(String[]args){

        System.out.println("I LOVE \"JAVA\" Programming");// Here we use \" to put qutations around java

        System.out.println("I like to \'eat \' \\cake"); // this puts a single quote around cook
        System.out.println("Tomorrow is Friday.\nIt is a day off"); // the \n make the letters go to next line

        System.out.println("\t\t\tMessage");
    }
}
